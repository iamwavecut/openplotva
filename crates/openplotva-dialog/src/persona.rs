#[must_use]
pub fn daily_persona_for_day(chat_id: i64, day: i64) -> DailyPersona {
    let mut normalized_id = chat_id;
    if normalized_id < 0 {
        normalized_id = -(normalized_id + 1);
    }
    let mut indices = (0..DAILY_PERSONAS.len()).collect::<Vec<_>>();
    indices.sort_by_key(|idx| shuffle_key(normalized_id, *idx));
    if indices.is_empty() {
        return DailyPersona::default();
    }
    let day_index = day.rem_euclid(indices.len() as i64) as usize;
    raw_daily_persona(DAILY_PERSONAS[indices[day_index]])
}

#[must_use]
pub fn daily_persona_for_unix_timestamp(chat_id: i64, unix_timestamp: i64) -> DailyPersona {
    daily_persona_for_day(chat_id, unix_timestamp / 86_400)
}

fn raw_daily_persona(raw: RawDailyPersona) -> DailyPersona {
    DailyPersona {
        name: raw.name.to_owned(),
        background: raw.background.to_owned(),
        tone: raw.tone.to_owned(),
        boundaries: raw.boundaries.to_owned(),
    }
}

fn shuffle_key(chat_id: i64, idx: usize) -> u64 {
    let mut value = (chat_id as u64).wrapping_shl(32) ^ idx as u64;
    value = value.wrapping_add(0x9e37_79b9_7f4a_7c15);
    value = (value ^ (value >> 30)).wrapping_mul(0xbf58_476d_1ce4_e5b9);
    value = (value ^ (value >> 27)).wrapping_mul(0x94d0_49bb_1331_11eb);
    value ^ (value >> 31)
}

