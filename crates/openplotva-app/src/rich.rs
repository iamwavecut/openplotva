        assert!(
            many.starts_with(
                "<p><tg-emoji emoji-id=\"5298651821080879865\">✨</tg-emoji> 2 из 2</p>"
            )
        );
        assert!(many.contains("<tg-collage>"));
        assert!(many.contains(r#"<img src="https://h/b.png"/>"#));
        assert!(many.contains("<figcaption>автор</figcaption>"));
