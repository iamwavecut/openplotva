//! Rich-message composition and sending for OpenPlotva.
//!
//! [`RichMessenger`] is the single send facade for Telegram Rich Messages: it sanitizes
//! HTML at the boundary and delegates to the raw rich Bot API client, the streaming-draft
//! path (private chats), and the media uploader. The free `compose_*` functions build the
//! approved rich layouts (currency table, song, gallery, draw progress, leaderboard) as
//! sanitized rich HTML; they are pure and unit-tested so the visual contract is pinned
//! independently of the send path.

use std::future::Future;
use std::pin::Pin;

use openplotva_media::uploader::{UploaderClient, UploaderError};
use openplotva_telegram::{
    RichApiClient, RichApiError, RichMessage, RichSendOptions, escape_telegram_html_text,
    format_rich_html, sanitize_rich_html,
};
use serde_json::Value;

/// Send facade for rich messages.
#[derive(Clone, Debug)]
pub struct RichMessenger {
    rich: RichApiClient,
    uploader: UploaderClient,
}

impl RichMessenger {
    #[must_use]
    pub fn new(rich: RichApiClient, uploader: UploaderClient) -> Self {
        Self { rich, uploader }
    }

    /// Access the media uploader (to publish generated bytes/URLs before composing media).
    #[must_use]
    pub fn uploader(&self) -> &UploaderClient {
        &self.uploader
    }

    /// Send a rich message, sanitizing the HTML at the boundary.
    pub async fn send(
        &self,
        chat_id: i64,
        html: &str,
        options: &RichSendOptions,
    ) -> Result<RichMessage, RichApiError> {
        self.rich
            .send_rich_message(chat_id, &format_rich_html(html), options)
            .await
    }

    /// Replace a message's content with new rich HTML in place (group pseudo-streaming).
    pub async fn edit(
        &self,
        chat_id: i64,
        message_id: i64,
        html: &str,
        reply_markup: Option<Value>,
    ) -> Result<(), RichApiError> {
        self.rich
            .edit_message_text_rich(chat_id, message_id, &format_rich_html(html), reply_markup)
            .await
    }

    /// Stream a partial rich message into an animated draft (private chats only).
    pub async fn draft(
        &self,
        chat_id: i64,
        draft_id: i64,
        html: &str,
        message_thread_id: Option<i64>,
    ) -> Result<(), RichApiError> {
        self.rich
            .send_rich_message_draft(
                chat_id,
                draft_id,
                &format_rich_html(html),
                message_thread_id,
            )
            .await
    }
}

/// Boxed future returned by [`RichSender`] send/edit/draft methods.
pub type RichSendFuture<'a, T> = Pin<Box<dyn Future<Output = Result<T, RichApiError>> + Send + 'a>>;
/// Boxed future returned by [`RichSender`] upload methods.
pub type RichUploadFuture<'a> =
    Pin<Box<dyn Future<Output = Result<String, UploaderError>> + Send + 'a>>;

/// Abstraction over rich sending so handlers/workers can be unit-tested with a mock
/// instead of issuing real Bot API / uploader HTTP calls.
pub trait RichSender: Send + Sync + std::fmt::Debug {
    /// Send a rich message; resolves to the sent message id.
    fn send_rich<'a>(
        &'a self,
        chat_id: i64,
        html: &'a str,
        options: &'a RichSendOptions,
    ) -> RichSendFuture<'a, i64>;
    /// Replace a message's rich content in place.
    fn edit_rich<'a>(
        &'a self,
        chat_id: i64,
        message_id: i64,
        html: &'a str,
        reply_markup: Option<Value>,
    ) -> RichSendFuture<'a, ()>;
    /// Stream a partial rich draft (private chats only).
    fn draft_rich<'a>(
        &'a self,
        chat_id: i64,
        draft_id: i64,
        html: &'a str,
        message_thread_id: Option<i64>,
    ) -> RichSendFuture<'a, ()>;
    /// Publish bytes to the media host; resolves to a public HTTPS URL.
    fn upload_bytes<'a>(
        &'a self,
        bytes: Vec<u8>,
        content_type: &'a str,
        explicit_name: Option<&'a str>,
    ) -> RichUploadFuture<'a>;
    /// Hand a remote URL to the media host; resolves to a public HTTPS URL.
    fn upload_url<'a>(
        &'a self,
        source_url: &'a str,
        explicit_name: Option<&'a str>,
    ) -> RichUploadFuture<'a>;
}

impl RichSender for RichMessenger {
    fn send_rich<'a>(
        &'a self,
        chat_id: i64,
        html: &'a str,
        options: &'a RichSendOptions,
    ) -> RichSendFuture<'a, i64> {
        Box::pin(async move { Ok(self.send(chat_id, html, options).await?.id) })
    }

    fn edit_rich<'a>(
        &'a self,
        chat_id: i64,
        message_id: i64,
        html: &'a str,
        reply_markup: Option<Value>,
    ) -> RichSendFuture<'a, ()> {
        Box::pin(async move { self.edit(chat_id, message_id, html, reply_markup).await })
    }

    fn draft_rich<'a>(
        &'a self,
        chat_id: i64,
        draft_id: i64,
        html: &'a str,
        message_thread_id: Option<i64>,
    ) -> RichSendFuture<'a, ()> {
        Box::pin(async move { self.draft(chat_id, draft_id, html, message_thread_id).await })
    }

    fn upload_bytes<'a>(
        &'a self,
        bytes: Vec<u8>,
        content_type: &'a str,
        explicit_name: Option<&'a str>,
    ) -> RichUploadFuture<'a> {
        Box::pin(async move {
            self.uploader
                .upload_bytes(bytes, content_type, explicit_name)
                .await
        })
    }

    fn upload_url<'a>(
        &'a self,
        source_url: &'a str,
        explicit_name: Option<&'a str>,
    ) -> RichUploadFuture<'a> {
        Box::pin(async move { self.uploader.upload_url(source_url, explicit_name).await })
    }
}

